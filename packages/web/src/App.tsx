type Route =
  | { name: "front" }
  | { name: "auth"; next?: string }
  | { name: "consent" }
  | { name: "onboarding" }
  | { name: "vehicles" }
  | { name: "vehicle"; vehicleId: string }
  | { name: "documents" }
  | { name: "publicqr" };

type HttpState = {
  loading: boolean;
  status?: number;
  text?: string;
};

function parseHashRoute(hash: string): Route {
  const h = (hash || "").replace(/^#/, "");
  if (!h || h === "/") return { name: "front" };

  const [path, query] = h.split("?");
  const clean = (path || "").replace(/^\/+/, "");
  const [seg1, seg2] = clean.split("/");

  if (seg1 === "auth") {
    const params = new URLSearchParams(query || "");
    const next = params.get("next") || undefined;
    return { name: "auth", next };
  }
  if (seg1 === "consent") return { name: "consent" };
  if (seg1 === "onboarding") return { name: "onboarding" };
  if (seg1 === "vehicles") return { name: "vehicles" };
  if (seg1 === "vehicle" && seg2) return { name: "vehicle", vehicleId: seg2 };
  if (seg1 === "documents") return { name: "documents" };
  if (seg1 === "publicqr") return { name: "publicqr" };

  // Fallback: unknown -> front
  return { name: "front" };
}

function setHashRoute(route: Route) {
  switch (route.name) {
    case "front":
      window.location.hash = "#/";
      return;
    case "auth": {
      const next = route.next ? `?next=${encodeURIComponent(route.next)}` : "";
      window.location.hash = `#/auth${next}`;
      return;
    }
    case "consent":
      window.location.hash = "#/consent";
      return;
    case "onboarding":
      window.location.hash = "#/onboarding";
      return;
    case "vehicles":
      window.location.hash = "#/vehicles";
      return;
    case "vehicle":
      window.location.hash = `#/vehicle/${encodeURIComponent(route.vehicleId)}`;
      return;
    case "documents":
      window.location.hash = "#/documents";
      return;
    case "publicqr":
      window.location.hash = "#/publicqr";
      return;
  }
}

function useHashRoute(): Route {
  const [route, setRoute] = useState<Route>(() => parseHashRoute(window.location.hash));

  useEffect(() => {
    const onHash = () => setRoute(parseHashRoute(window.location.hash));
    window.addEventListener("hashchange", onHash);
    return () => window.removeEventListener("hashchange", onHash);
  }, []);

  return route;
}
