function useHttpState() {
  const [state, setState] = useState<HttpState>({ loading: false });

  async function run<T>(fn: () => Promise<T>) {
    setState({ loading: true });
    try {
      const res = await fn();
      setState({ loading: false, status: 200, text: "OK" });
      return res;
    } catch (e: any) {
      const status = typeof e?.status === "number" ? e.status : undefined;
      setState({ loading: false, status, text: prettyBody(e) });
      throw e;
    }
  }

  return { state, run };
}

